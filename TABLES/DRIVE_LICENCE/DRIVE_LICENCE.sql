
CREATE TABLE DRIVE_LICENCE 
(
    LICENCE_ID NUMBER(10) GENERATED BY DEFAULT AS IDENTITY,
    LICENCE_TAKEN_DATE DATE NOT NULL,
    LICENCE_TYPE_ID NUMBER(10) NOT NULL,
    CUSTOMER_ID NUMBER(10) NOT NULL,
    PRIMARY KEY (LICENCE_ID),
    
    CONSTRAINT LICENCE_TYPE_FK
    FOREIGN KEY(LICENCE_TYPE_ID) REFERENCES DRIVE_LICENCE_TYPE (LICENCE_TYPE_ID)
    ON DELETE CASCADE,
    
    CONSTRAINT CUSTOMER_FK
    FOREIGN KEY (CUSTOMER_ID) REFERENCES CUSTOMER(CUSTOMER_ID)
    ON DELETE CASCADE
    
);
