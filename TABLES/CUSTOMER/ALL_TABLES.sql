CREATE TABLE CUSTOMER_LOGIN
(
    LOGIN_ID NUMBER(10) GENERATED BY DEFAULT AS IDENTITY,
    EMAIL VARCHAR2(50) NOT NULL UNIQUE,
    CUSTOMER_PASSWORD VARCHAR2(30) NOT NULL,
    CREATED_DATE DATE DEFAULT (SYSDATE),
    PRIMARY KEY (LOGIN_ID)
    
);



CREATE TABLE CUSTOMER 
(
    CUSTOMER_ID NUMBER(10) GENERATED BY DEFAULT AS IDENTITY,
    LOGIN_ID NUMBER(10) NOT NULL UNIQUE,
    FIRST_NAME VARCHAR2(50) NOT NULL,
    LAST_NAME VARCHAR2(30) NOT NULL,
    IDENTITY_NUMBER NUMBER NOT NULL,
    BIRTHDAY DATE NOT NULL,
    CREATED_DATE DATE DEFAULT (SYSDATE),
    PRIMARY KEY (CUSTOMER_ID),

    
    CONSTRAINT FK_LOGIN_ID
    FOREIGN KEY (LOGIN_ID) REFERENCES CUSTOMER_LOGIN(LOGIN_ID)
    ON DELETE CASCADE
);

CREATE TABLE CUSTOMER_ADRESS 
(
    ADRESS_ID NUMBER(10) GENERATED BY DEFAULT AS IDENTITY,
    CUSTOMER_ID NUMBER(10) NOT NULL,
    COUNTRY VARCHAR2(50) NOT NULL,
    CITY VARCHAR2(50) NOT NULL,
    TOWN VARCHAR2(50) NOT NULL,
    STREET VARCHAR2(50) NOT NULL,
    BUILDING_INFO VARCHAR2(50) NOT NULL,
    DOOR_NUMBER NUMBER(5) NOT NULL,
    ZIP_CODE NUMBER(10) NOT NULL,
    CREATED_DATE DATE DEFAULT (SYSDATE),
    
    CONSTRAINT PRIMARY_KEY
    PRIMARY KEY (ADRESS_ID),
    
    CONSTRAINT FK_CUSTOMER_ID
    FOREIGN KEY (CUSTOMER_ID) REFERENCES SYS.customer(CUSTOMER_ID)
    ON DELETE CASCADE 
    
)



