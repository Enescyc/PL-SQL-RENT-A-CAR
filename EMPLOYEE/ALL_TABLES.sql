CREATE TABLE EMPLOYEE_LOGIN 

(
    EMPLOYEE_LOGIN_ID NUMBER(10) GENERATED BY DEFAULT AS IDENTITY ,
    EMPLOYEE_EMAIL VARCHAR2(40) NOT NULL UNIQUE,
    EMPLOYEE_PASSWORD VARCHAR(40) NOT NULL,
    
    
    CONSTRAINT PK_EMPLOYEE_LOGIN_ID
    PRIMARY KEY (EMPLOYEE_LOGIN_ID)
)


CREATE TABLE EMPLOYEE 
(
    EMPLOYEE_ID NUMBER (10) GENERATED BY DEFAULT AS IDENTITY,
    LOGIN_ID NUMBER(10),
    FIRST_NAME VARCHAR2(40) NOT NULL,
    LAST_NAME VARCHAR(40) NOT NULL,
    BIRTHDAY DATE ,
    TEAM VARCHAR2(40),
    
    CONSTRAINT PK_EMPLOYE
    PRIMARY KEY (EMPLOYEE_ID),
    
    CONSTRAINT FK_EMPLOYEE_LOGIN_ID
    FOREIGN KEY (LOGIN_ID) REFERENCES EMPLOYEE_LOGIN(EMPLOYEE_LOGIN_ID)
    ON DELETE CASCADE
)


CREATE TABLE EMPLOYEE_ROLE 
(
    ROLE_ID NUMBER (10) GENERATED BY DEFAULT AS IDENTITY,
    EMPLOYEE_ROLE VARCHAR(40)NOT NULL,

    CONSTRAINT PK_EMPLOYEE_ROLE
    PRIMARY KEY (ROLE_ID)
    
)

CREATE TABLE EMPLOYEE_AND_ROLE 

(
    EMPLOYEE_ROLE_ID NUMBER (10) GENERATED BY DEFAULT AS IDENTITY,
    EMPLOYEE_ID NUMBER(10),
    ROLE_ID NUMBER(10),
    
    
    CONSTRAINT PK_EMPLOYEE_AND_ROLE
    PRIMARY KEY(EMPLOYEE_ROLE_ID),
    
    
    CONSTRAINT FK_ROLE_EMPLOYEE_ID
    FOREIGN KEY (EMPLOYEE_ID) REFERENCES EMPLOYEE (EMPLOYEE_ID)
    ON DELETE CASCADE,
    
    CONSTRAINT FK_ROLE_ID
    FOREIGN KEY (ROLE_ID) REFERENCES EMPLOYEE_ROLE (ROLE_ID)
    ON DELETE CASCADE
    
    
)